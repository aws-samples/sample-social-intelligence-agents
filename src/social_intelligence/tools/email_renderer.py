"""HTML email renderer: converts EmailDraft Pydantic models into professional HTML emails.

Uses Jinja2 with inline CSS for maximum email client compatibility.
Generates standalone HTML files that can be previewed in any browser
or sent via Amazon SES/SMTP.

Security: Jinja2 autoescape is enabled (select_autoescape) to prevent XSS in
generated HTML. All template variables are escaped by default. Callers should
validate prospect data before rendering. For production Amazon SES delivery,
add CAN-SPAM compliance elements (unsubscribe link, physical address).

Compliance footer: set COMPLIANCE_FOOTER_REQUIRED=true to append an unsubscribe
link and physical address to every rendered email (CAN-SPAM / CASL compliance).
Default is off to preserve exact blog behavior.
"""

import json
import logging
import os
from datetime import datetime, timezone

from strands import tool

logger = logging.getLogger(__name__)

# Cached compiled Jinja2 template: created once on first render, reused thereafter.
_cached_template = None

