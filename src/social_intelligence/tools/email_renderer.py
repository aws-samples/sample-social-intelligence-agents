def render_email_html(
    prospect_id: str,
    subject: str,
    body: str,
    personalization_tokens: list[str] | None = None,
    brand_compliant: bool = True,
    score: int = 0,
    confidence: float = 0.0,
    data_quality: str = "medium",
    brand_name: str = "AnyCompany",
) -> str:
    """Render an EmailDraft into a professional HTML email.

    Use this after the email generation agent produces an EmailDraft to create
    a polished, email-client-compatible HTML version. The output can be previewed
    in a browser or sent via Amazon SES.

    Args:
        prospect_id: Prospect identifier.
        subject: Email subject line.
        body: Email body text (plain text, will be split into paragraphs).
        personalization_tokens: Data points referenced in the email.
        brand_compliant: Whether the email passed brand guidelines.
        score: Prospect relevance score (0-100).
        confidence: Score confidence (0.0-1.0).
        data_quality: Data quality assessment.
        brand_name: Brand name for the header.
    """
    from jinja2 import Environment, select_autoescape

    # Module-level cache for the compiled template (created once, reused)
    global _cached_template  # noqa: PLW0603
    if _cached_template is None:
        # nosemgrep: python.flask.security.xss.audit.direct-use-of-jinja2
        # autoescape is explicitly enabled below, so HTML escaping is NOT bypassed.
        # This renders a self-contained email template (not a Flask response), so
        # render_template() does not apply. False positive; escaping is verified on.
        env = Environment(
            autoescape=select_autoescape(default_for_string=True, default=True),
        )
        _cached_template = env.from_string(EMAIL_TEMPLATE)

    # Split body into paragraphs
    body_paragraphs = [p.strip() for p in body.split("\n") if p.strip()]

    # Score color
    if score >= 80:
        score_color = "#38a169"  # green
    elif score >= 60:
        score_color = "#d69e2e"  # yellow
    else:
        score_color = "#e53e3e"  # red

    template = _cached_template
    compliance_footer = bool(os.environ.get("COMPLIANCE_FOOTER_REQUIRED", ""))
    unsubscribe_url = os.environ.get("UNSUBSCRIBE_URL", "https://example.com/unsubscribe")

    html = template.render(
        brand_name=brand_name,
        subject=subject,
        body_paragraphs=body_paragraphs,
        personalization_tokens=personalization_tokens or [],
        score=score,
        score_color=score_color,
        confidence=f"{confidence:.0%}" if confidence else "N/A",
        data_quality=data_quality,
        prospect_id=prospect_id,
        brand_compliant_text="Brand compliant" if brand_compliant else "Needs review",
        generated_at=datetime.now(timezone.utc).strftime("%Y-%m-%d %H:%M UTC"),
        compliance_footer=compliance_footer,
        unsubscribe_url=unsubscribe_url,
        physical_address=_COMPLIANCE_PHYSICAL_ADDRESS,
    )

    return json.dumps(
        {
            "html": html,
            "prospect_id": prospect_id,
            "subject": subject,
            "rendered": True,
        }
    )


@tool
def render_email_html_tool(
    prospect_id: str,
    subject: str,
    body: str,
    personalization_tokens: list[str] | None = None,
    score: int = 0,
    confidence: float = 0.0,
    data_quality: str = "medium",
) -> str:
    """Render an email draft into professional HTML for preview or Amazon SES delivery.

    Args:
        prospect_id: Prospect identifier.
        subject: Email subject line.
        body: Email body text (paragraphs separated by newlines).
        personalization_tokens: Ordered data points referenced in the email.
        score: Prospect relevance score (0-100).
        confidence: Score confidence (0.0-1.0).
        data_quality: Data quality assessment (high/medium/low).
    """
    if isinstance(personalization_tokens, str):
        tokens = [token.strip() for token in personalization_tokens.split(",") if token.strip()]
    else:
        tokens = [str(token).strip() for token in personalization_tokens or [] if str(token).strip()]
    return render_email_html(
        prospect_id=prospect_id,
        subject=subject,
        body=body,
        personalization_tokens=tokens,
        score=score,
        confidence=confidence,
        data_quality=data_quality,
    )
